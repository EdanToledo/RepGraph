package com.RepGraph;

public class anchors {
    private String from;
    private String end;

    public anchors(String from, String end) {
        this.from = from;
        this.end = end;
    }

    public String getFrom() {
        return from;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public void setFrom(String from) {
        this.from = from;
    }


}
